int mp6_savestate_restore(const char *path)
{
    Mp6SavestateRegion fileRegions[MP6_SAVESTATE_MAX_REGIONS];
    Mp6SavestateHeader hdr;
    FILE *f = NULL;
    unsigned char *raw = NULL;
    unsigned char *inBuf = NULL;
    z_stream zs;
    int zInit = 0, rc = MP6_SAVESTATE_OK, i;
    uint64_t off;
    const size_t kChunk = 1u << 20;

    if (path == NULL) {
        return MP6_SAVESTATE_ERR_IO;
    }
    f = fopen(path, "rb");
    if (f == NULL) {
        return MP6_SAVESTATE_ERR_IO;
    }
    if (fread(&hdr, sizeof(hdr), 1, f) != 1) {
        rc = MP6_SAVESTATE_ERR_FORMAT;
        goto done;
    }
    if (hdr.magic != MP6_SAVESTATE_MAGIC || hdr.version != MP6_SAVESTATE_VERSION ||
        hdr.regionCount == 0 || hdr.regionCount > MP6_SAVESTATE_MAX_REGIONS) {
        rc = MP6_SAVESTATE_ERR_FORMAT;
        goto done;
    }
    if (strncmp(hdr.buildStamp, mp6_link_stamp, sizeof(hdr.buildStamp)) != 0) {
        fprintf(stderr, "[SAVESTATE] refusing: state built %s, this exe built %s\n",
                hdr.buildStamp, mp6_link_stamp);
        rc = MP6_SAVESTATE_ERR_BINARY_MISMATCH;
        goto done;
    }
    /* Address pinning. These are MEASURED-stable, not guaranteed -- so they
     * are re-checked here every single load, and a mismatch is refused
     * loudly rather than "relocated" by guesswork. A 256 MB window is wide
     * enough that ordinary integers alias into it, so a blind
     * add-a-delta-to-anything-in-range pass would have a real
     * false-positive rate and could corrupt state silently. Refusing is the
     * honest outcome. */
    if (hdr.arenaBase != (uint64_t)(uintptr_t)mp6_arena_base() ||
        hdr.arenaSize != (uint64_t)mp6_arena_size() ||
        hdr.coroPoolBase != (uint64_t)(uintptr_t)mp6_coro_pool_base() ||
        hdr.imageBase != mp6_ss_image_base_proxy()) {
        fprintf(stderr,
                "[SAVESTATE] refusing: address layout differs from capture "
                "(arena %llx vs %llx, coro %llx vs %llx, image %llx vs %llx)\n",
                (unsigned long long)hdr.arenaBase, (unsigned long long)(uintptr_t)mp6_arena_base(),
                (unsigned long long)hdr.coroPoolBase, (unsigned long long)(uintptr_t)mp6_coro_pool_base(),
                (unsigned long long)hdr.imageBase, (unsigned long long)mp6_ss_image_base_proxy());
        rc = MP6_SAVESTATE_ERR_LAYOUT_MISMATCH;
        goto done;
    }
    if (fread(fileRegions, sizeof(fileRegions[0]), hdr.regionCount, f) != hdr.regionCount) {
        rc = MP6_SAVESTATE_ERR_FORMAT;
        goto done;
    }
    /* Validate the WHOLE region table against the live process. The
     * table is uncompressed and sits OUTSIDE the
     * adler-protected deflate stream, so a bit-flip or hand-truncation here
     * could survive every header check -- and each non-CORO entry is a
     * memcpy DESTINATION. The earlier revision checked only CORO addresses
     * and size!=0, which left the commit loop trusting file-supplied
     * addresses; that is a wild write, not fail-closed.
     *
     * Method: re-collect this process's own region set (the same function
     * capture used, so the two literally cannot drift) and require the
     * file's non-CORO entries to match it one-for-one, in order. CORO
     * entries are validated by FORMULA instead (slot address arithmetic),
     * because the captured in-use slot set legitimately differs from the
     * restoring process's own. ARAM's addr is advisory (host heap, per-
     * process) -- size must match, address is ignored. Finally the region
     * sizes must sum exactly to hdr.rawBytes, or the commit loop's running
     * offset would walk off the scratch buffer. */
    {
        Mp6SavestateRegion expect[MP6_SAVESTATE_MAX_REGIONS];
        uint64_t expectRaw = 0, sum = 0;
        int nExpect, e = 0, coroSeen = 0;

        memset(expect, 0, sizeof(expect));
        nExpect = mp6_ss_collect_regions(expect, MP6_SAVESTATE_MAX_REGIONS, &expectRaw);
        if (nExpect <= 0) {
            rc = MP6_SAVESTATE_ERR_UNSUPPORTED;
            goto done;
        }
        for (i = 0; i < (int)hdr.regionCount; i++) {
            const Mp6SavestateRegion *fr = &fileRegions[i];
            if (fr->size == 0) {
                fprintf(stderr, "[SAVESTATE] refusing: region %d (%.*s) has zero size\n",
                        i, (int)sizeof(fr->name), fr->name);
                rc = MP6_SAVESTATE_ERR_FORMAT;
                goto done;
            }
            sum += fr->size;
            if (fr->kind == MP6_SS_REGION_CORO) {
                void *slotAddr = mp6_coro_slot_addr((int)fr->index);
                coroSeen = 1;
                if (slotAddr == NULL ||
                    (uint64_t)(uintptr_t)slotAddr != fr->addr ||
                    fr->size != (uint64_t)mp6_coro_slot_size()) {
                    fprintf(stderr, "[SAVESTATE] refusing: coro region %d (slot %u) does not "
                                    "match this process's pool layout\n", i, fr->index);
                    rc = MP6_SAVESTATE_ERR_LAYOUT_MISMATCH;
                    goto done;
                }
                continue;
            }
            if (coroSeen) {
                /* collect emits every non-CORO region before the coro slots;
                 * a non-CORO entry after one is not a table we wrote. */
                fprintf(stderr, "[SAVESTATE] refusing: region table order is not ours\n");
                rc = MP6_SAVESTATE_ERR_FORMAT;
                goto done;
            }
            /* Advance to this process's next non-CORO expected region. */
            while (e < nExpect && expect[e].kind == MP6_SS_REGION_CORO) {
                e++;
            }
            if (e >= nExpect ||
                fr->kind != expect[e].kind ||
                fr->size != expect[e].size ||
                (fr->kind != MP6_SS_REGION_ARAM && fr->addr != expect[e].addr)) {
                fprintf(stderr,
                        "[SAVESTATE] refusing: region %d (%.*s) does not match this build's "
                        "layout (file kind=%u addr=%llx size=%llu vs live kind=%u addr=%llx size=%llu)\n",
                        i, (int)sizeof(fr->name), fr->name,
                        fr->kind, (unsigned long long)fr->addr, (unsigned long long)fr->size,
                        e < nExpect ? expect[e].kind : 0,
                        e < nExpect ? (unsigned long long)expect[e].addr : 0ull,
                        e < nExpect ? (unsigned long long)expect[e].size : 0ull);
                rc = MP6_SAVESTATE_ERR_LAYOUT_MISMATCH;
                goto done;
            }
            e++;
        }
        /* Every live non-CORO region must have been consumed -- a file with
         * FEWER image sections than this binary would otherwise partially
         * restore the image. */
        while (e < nExpect && expect[e].kind == MP6_SS_REGION_CORO) {
            e++; /* the live process's own in-use coro slots are not required of the file */
        }
        if (e != nExpect) {
            fprintf(stderr, "[SAVESTATE] refusing: state file covers fewer regions than this build\n");
            rc = MP6_SAVESTATE_ERR_LAYOUT_MISMATCH;
            goto done;
        }
        if (sum != hdr.rawBytes) {
            fprintf(stderr, "[SAVESTATE] refusing: region sizes sum to %llu but header says %llu\n",
                    (unsigned long long)sum, (unsigned long long)hdr.rawBytes);
            rc = MP6_SAVESTATE_ERR_FORMAT;
            goto done;
        }
    }

    /* the audio shadow is header data no other check covers --
     * clamp its counts before anything iterates them. Capture clamps on
     * write, so an out-of-range value here can only mean corruption. */
    if (hdr.audio.chanCount < 0 || hdr.audio.chanCount > MP6_SS_AUDIO_MAX_CHAN ||
        hdr.audio.groupCount < 0 || hdr.audio.groupCount > MP6_SS_AUDIO_MAX_GROUPS) {
        fprintf(stderr, "[SAVESTATE] refusing: audio shadow counts out of range (chan=%d group=%d)\n",
                (int)hdr.audio.chanCount, (int)hdr.audio.groupCount);
        rc = MP6_SAVESTATE_ERR_FORMAT;
        goto done;
    }

    /* Decompress everything into scratch FIRST. Nothing below this point
     * touches live memory until the whole payload has been proven intact --
     * that is what makes a corrupt file a clean error instead of a
     * half-restored process.
     *
     * the single-shot inflate hands rawBytes to zlib's 32-bit
     * avail_out, so the guard must be UINT32_MAX -- the earlier >SIZE_MAX
     * test was vacuous on win64 and a >4GB state would have truncated the
     * window and failed with a misleading ERR_FORMAT. Latent today
     * (~300MB), but the failure it names is a size threshold, so name it. */
    if (hdr.rawBytes == 0 || hdr.rawBytes > 0xFFFFFFFFull) {
        fprintf(stderr, "[SAVESTATE] refusing: rawBytes %llu exceeds the 4GB single-shot "
                        "inflate limit\n",
                (unsigned long long)hdr.rawBytes);
        rc = MP6_SAVESTATE_ERR_FORMAT;
        goto done;
    }
    raw = (unsigned char *)malloc((size_t)hdr.rawBytes);
    inBuf = (unsigned char *)malloc(kChunk);
    if (raw == NULL || inBuf == NULL) {
        rc = MP6_SAVESTATE_ERR_NOMEM;
        goto done;
    }
    memset(&zs, 0, sizeof(zs));
    if (inflateInit(&zs) != Z_OK) {
        rc = MP6_SAVESTATE_ERR_NOMEM;
        goto done;
    }
    zInit = 1;
    zs.next_out = raw;
    zs.avail_out = (uInt)hdr.rawBytes;
    for (;;) {
        size_t got = fread(inBuf, 1, kChunk, f);
        int zr;
        if (got == 0) {
            rc = MP6_SAVESTATE_ERR_FORMAT; /* ran out of input before Z_STREAM_END */
            goto done;
        }
        zs.next_in = inBuf;
        zs.avail_in = (uInt)got;
        zr = inflate(&zs, Z_NO_FLUSH);
        if (zr == Z_STREAM_END) {
            break;
        }
        if (zr != Z_OK) {
            rc = MP6_SAVESTATE_ERR_FORMAT;
            goto done;
        }
    }
    if (zs.total_out != hdr.rawBytes) {
        rc = MP6_SAVESTATE_ERR_FORMAT;
        goto done;
    }

    /* Commit. From here on there is no failure path -- every destination
     * was validated above, and the source is fully materialized. */
    /* Latch every host-owned pointer we will need DURING the commit loop,
     * BEFORE the loop starts.
     *
     * This is not defensive style, it is required: the loop restores the
     * image's .bss early on, and g_fakeAram (platform/null/shims_manual.c)
     * lives there. Querying the ARAM buffer from inside the loop therefore
     * returns whatever the .bss restore just installed -- the CAPTURING
     * process's heap pointer -- and the memcpy writes 16 MB through it.
     * (Observed exactly that way: an access violation in memcpy at this
     * line, writing a foreign heap address.) Anything else this loop ever
     * needs from restorable memory must be latched here too. */
    {
        size_t latchedAramSize = 0;
        void *latchedAram = mp6_aram_savestate_buffer(&latchedAramSize);
        /* the tick BUDGET is a host run-parameter, not game
         * state -- it describes how long THIS invocation was asked to run
         * (a CLI arg, or "unlimited" for a double-click session). It lives
         * in restored .data, so without this latch a restore imports the
         * CAPTURING run's budget: an interactive session that loads a
         * scripted 4000-tick capture silently exits mid-play when the
         * rewound counter re-reaches 4000, and a scripted gate run that
         * loads an interactive capture inherits unlimited=1 and never
         * honors its budget. mp6_tick_count itself is deliberately NOT
         * latched -- rewinding it is load-bearing for the replay levers
         * and for determinism. */
        int latchedMaxTicks = mp6_max_ticks;
        int latchedUnlimited = mp6_ticks_unlimited;

    /* free THIS process's widescreen native snapshots while the live
     * registry pointers are still valid -- the commit loop is about to
     * overwrite them with the capturing process's (foreign) pointers, after
     * which the memory is unreachable and leaks on every load. */
    mp6_widescreen_savestate_prerestore();

    off = 0;
    for (i = 0; i < (int)hdr.regionCount; i++) {
        if (fileRegions[i].kind == MP6_SS_REGION_ARAM) {
            /* ARAM is the ONE region whose base is legitimately not pinned:
             * it is a host calloc, not a fixed-address reservation. Its
             * recorded address is therefore ADVISORY (diagnostics only) and
             * the payload goes into whatever buffer THIS process allocated.
             * Size must agree exactly -- a differing size means a different
             * build's ARAM constant, which the build stamp should already
             * have rejected, so this is belt-and-braces. */
            if (latchedAram != NULL && latchedAramSize == (size_t)fileRegions[i].size) {
                memcpy(latchedAram, raw + off, latchedAramSize);
            } else {
                fprintf(stderr, "[SAVESTATE] WARNING: ARAM buffer unavailable or size "
                                "mismatch (live=%zu file=%llu) -- ARAM contents NOT restored\n",
                        latchedAramSize, (unsigned long long)fileRegions[i].size);
            }
        } else {
            memcpy((void *)(uintptr_t)fileRegions[i].addr, raw + off, (size_t)fileRegions[i].size);
        }
        off += fileRegions[i].size;
    }

    /* Re-derive host-side state that the commit loop just desynchronized.
     * Order is load-bearing -- see mp6_savestate.h. ARAM first because the
     * .bss restore above just clobbered g_fakeAram with the capturing
     * process's pointer, and anything that touches ARAM afterwards would
     * follow it. We hand back the LATCHED buffer (the one the loop actually
     * copied the restored bytes into), not a freshly allocated one -- an
     * allocate-a-new-one rehydrate would throw those bytes away and leave
     * ARAM zeroed while the restored residency table insisted it was full. */
    mp6_aram_savestate_rehydrate(latchedAram);

    /* put the RUNNING invocation's tick budget back (latched above). */
    mp6_max_ticks = latchedMaxTicks;
    mp6_ticks_unlimited = latchedUnlimited;
    }

    mp6_dvd_savestate_rehydrate();

#ifndef MP6_HEADLESS_BUILD
    /* FINDING #1 (savestate-x1): drop the Unlocked FPS interpolation state on
     * restore. frame_interp.c's pacing statics are carved out of the image (so
     * the commit loop above did NOT clobber them), but the retained N-1/N model
     * snapshots still describe PRE-restore frames -- clear them so the first
     * post-restore window starts clean instead of interpolating across the
     * state discontinuity. Windowed build only (frame_interp.c is aurora-only;
     * nothing to reset headless -- the model module never replays there). */
    {
        extern void mp6_fi_savestate_reset(void);
        mp6_fi_savestate_reset();
    }
#endif

    /* rebuild the widescreen native snapshots from the file's blob
     * (offset = header + table + deflate stream; hdr.compressedBytes is the
     * key -- its first real consumer). Every entry the blob cannot cover
     * fails soft to the apply early-out: frozen geometry, never corruption
     * and never a foreign pointer. */
    {
        long blobOff = (long)(sizeof(Mp6SavestateHeader)
                              + (size_t)hdr.regionCount * sizeof(Mp6SavestateRegion)
                              + (size_t)hdr.compressedBytes);
        void *wsBlob = NULL;
        size_t wsSize = (size_t)hdr.wsNativeBytes;
        if (wsSize > 0 && wsSize < (64u << 20) && fseek(f, blobOff, SEEK_SET) == 0) {
            wsBlob = malloc(wsSize);
            if (wsBlob != NULL && fread(wsBlob, 1, wsSize, f) != wsSize) {
                free(wsBlob);
                wsBlob = NULL;
            }
        }
        /* Called even with a NULL blob: it must still clear the foreign
         * pointers the image restore just installed. */
        mp6_widescreen_savestate_apply_natives(wsBlob, wsBlob ? wsSize : 0);
        free(wsBlob);
    }
    /* Audio LAST: re-establishing a stream issues DVD reads (msmStreamPlay ->
     * decode_substream -> path resolution -> the FST), so it depends on the
     * DVD layer above already being rehydrated. */
    mp6_msm_savestate_apply(&hdr.audio);

    g_wasRestored = 1;
    printf("[SAVESTATE] restored %s: %d regions, %.1f MB (rehydrated aram/dvd/widescreen)\n",
           path, (int)hdr.regionCount, (double)hdr.rawBytes / (1024.0 * 1024.0));
    fflush(stdout);

done:
    if (zInit) {
        inflateEnd(&zs);
    }
    free(raw);
    free(inBuf);
    if (f) {
        fclose(f);
    }
    return rc;
}

/* ==================================================================
 * Frame-boundary driver
 * ==================================================================
 * NOTE ON THIS FILE'S OWN STATICS. savestate.c is force-included with
 * mp6_host_section.h, so everything below lives in the host-owned carve-out
 * section and is NOT itself captured or restored. That is load-bearing, not
 * incidental: g_pending is set by the hotkey handler and consumed here, so
 * if it were part of the captured image it would be restored along with
 * everything else -- and a state captured while a request was in flight
 * would re-trigger that same request on every load, forever. Carving this
 * file out makes the request flag belong to the RUNNING process, which is
 * the only process it can meaningfully refer to.
 */
enum { MP6_SS_PENDING_NONE = 0, MP6_SS_PENDING_SAVE, MP6_SS_PENDING_LOAD };
static int g_pending;
static int g_tickLeversParsed;   /* -1/0/1 lazy-parse, matching this port's env-lever house style */
static long g_saveAtTick = -1;
static long g_loadAtTick = -1;
/* Fire-once latches for the scripted levers. REQUIRED, not defensive: a
 * restore rewinds mp6_tick_count to its captured value, so a bare
 * `mp6_tick_count == g_loadAtTick` test re-arms itself the moment the
 * replayed run counts back up to that tick -- an infinite restore loop
 * (observed, not theorized: the first version of this file hung exactly
 * this way). These latches live in the host-owned carve-out section along
 * with the rest of this file's statics, so a restore does NOT reset them --
 * which is precisely what makes them able to break the cycle. */
static int g_saveFired;
static int g_loadFired;
static char g_slotPath[512];
/* UI slot-request extension (mp6_savestate.h "Slot-file UI seam"): the
 * pending request's target path (empty = default slot path), and the
 * one-shot last-result pickup the in-game Save States page consumes. All
 * carved-out for the same reason g_pending is: they describe the RUNNING
 * process's UI conversation, which a restored image must not overwrite. */
static char g_requestPath[520];
static int g_lastResultValid;
static int g_lastResultWasSave;
static int g_lastResultCode;
static unsigned int g_uiGeneration;

/* The tick counter both build modes advance (platform/null/shims_manual.c),
 * used only by the scripted env levers below. */
extern long mp6_tick_count;

static const char *mp6_ss_slot_path(void)
{
    if (g_slotPath[0] == '\0') {
        const char *env = getenv("MP6_SAVESTATE_PATH");
        snprintf(g_slotPath, sizeof(g_slotPath), "%s",
                 (env && env[0]) ? env : "mp6_savestate.mp6state");
    }
    return g_slotPath;
}

void mp6_savestate_request_save(void) { mp6_savestate_request_save_path(NULL); }
void mp6_savestate_request_load(void) { mp6_savestate_request_load_path(NULL); }

void mp6_savestate_request_save_path(const char *path)
{
    snprintf(g_requestPath, sizeof(g_requestPath), "%s", (path && path[0]) ? path : "");
    g_pending = MP6_SS_PENDING_SAVE;
}

void mp6_savestate_request_load_path(const char *path)
{
    snprintf(g_requestPath, sizeof(g_requestPath), "%s", (path && path[0]) ? path : "");
    g_pending = MP6_SS_PENDING_LOAD;
}

void mp6_savestate_slot_file(int slot, char *buf, size_t n)
{
    const char *base = mp6_ss_slot_path();
    size_t len = strlen(base);
    const char *suffix = ".mp6state";
    size_t sufLen = strlen(suffix);
    if (len > sufLen && strcmp(base + len - sufLen, suffix) == 0) {
        snprintf(buf, n, "%.*s_slot%d%s", (int)(len - sufLen), base, slot, suffix);
    } else {
        snprintf(buf, n, "%s_slot%d", base, slot);
    }
}

int mp6_savestate_probe(const char *path)
{
    Mp6SavestateHeader hdr;
    FILE *f;
    if (path == NULL) {
        return MP6_SAVESTATE_ERR_IO;
    }
    f = fopen(path, "rb");
    if (f == NULL) {
        return MP6_SAVESTATE_ERR_IO;
    }
    if (fread(&hdr, sizeof(hdr), 1, f) != 1) {
        fclose(f);
        return MP6_SAVESTATE_ERR_FORMAT;
    }
    fclose(f);
    if (hdr.magic != MP6_SAVESTATE_MAGIC || hdr.version != MP6_SAVESTATE_VERSION) {
        return MP6_SAVESTATE_ERR_FORMAT;
    }
    if (strncmp(hdr.buildStamp, mp6_link_stamp, sizeof(hdr.buildStamp)) != 0) {
        return MP6_SAVESTATE_ERR_BINARY_MISMATCH;
    }
    return MP6_SAVESTATE_OK;
}

int mp6_savestate_take_last_result(int *wasSave, int *result)
{
    if (!g_lastResultValid) {
        return 0;
    }
    g_lastResultValid = 0;
    if (wasSave) *wasSave = g_lastResultWasSave;
    if (result) *result = g_lastResultCode;
    return 1;
}

unsigned int mp6_savestate_ui_generation(void) { return g_uiGeneration; }

/* parse a tick lever strictly, or refuse LOUDLY and stay
 * unarmed. Bare strtol let "3OO" parse as 3 and a fully non-numeric value
 * parse as 0 -- and 0 passes the >=0 arming test, capturing at the first
 * tick, which (save and load sharing one slot path) could overwrite the
 * very state file the user was about to load. Fail-closed means a typo
 * must disable the lever, not fire it somewhere surprising. */
static long mp6_ss_parse_tick_env(const char *name)
{
    const char *v = getenv(name);
    char *end = NULL;
    long parsed;

    if (v == NULL || v[0] == '\0') {
        return -1;
    }
    parsed = strtol(v, &end, 10);
    if (end == v || *end != '\0' || parsed < 0) {
        fprintf(stderr, "[SAVESTATE] %s=\"%s\" is not a valid tick number -- lever DISABLED "
                        "(a mis-parse must not fire at a surprise tick)\n", name, v);
        fflush(stderr);
        return -1;
    }
    return parsed;
}

void mp6_savestate_tick(void)
{
    int action;

    if (!g_tickLeversParsed) {
        g_saveAtTick = mp6_ss_parse_tick_env("MP6_SAVESTATE_SAVE_AT_TICK");
        g_loadAtTick = mp6_ss_parse_tick_env("MP6_SAVESTATE_LOAD_AT_TICK");
        g_tickLeversParsed = 1;
    }

    /* Scripted levers (the regression gate uses these -- they work in the
     * headless build too, where there is no window to press a key in).
     * only arm a lever when no request is already queued --
     * the levers used to overwrite g_pending unconditionally, silently
     * discarding an F5/F8 the user pressed on the same tick. The >= trigger
     * plus the fire-once latch means a deferred lever simply fires on the
     * next tick instead. */
    if (g_pending == MP6_SS_PENDING_NONE) {
        if (!g_saveFired && g_saveAtTick >= 0 && mp6_tick_count >= g_saveAtTick) {
            g_saveFired = 1;
            g_pending = MP6_SS_PENDING_SAVE;
        } else if (!g_loadFired && g_loadAtTick >= 0 && mp6_tick_count >= g_loadAtTick) {
            g_loadFired = 1;
            g_pending = MP6_SS_PENDING_LOAD;
        }
    }

    if (g_pending == MP6_SS_PENDING_NONE) {
        return;
    }
    action = g_pending;
    g_pending = MP6_SS_PENDING_NONE; /* cleared BEFORE acting: a failing
                                      * request must not retry every tick */

    {
        /* Consume the per-request target path (UI slot requests); empty =
         * the default slot path, exactly the pre-slot behavior. Latched
         * into a local BEFORE acting so a restore (which does not touch
         * these carved-out statics anyway) can never confuse it. */
        const char *path = g_requestPath[0] ? g_requestPath : mp6_ss_slot_path();
        int r;
        if (action == MP6_SS_PENDING_SAVE) {
            r = mp6_savestate_capture(path);
            if (r != MP6_SAVESTATE_OK) {
                fprintf(stderr, "[SAVESTATE] save failed: %s\n", mp6_savestate_strerror(r));
                fflush(stderr);
            }
        } else {
            r = mp6_savestate_restore(path);
            if (r != MP6_SAVESTATE_OK) {
                fprintf(stderr, "[SAVESTATE] load failed: %s\n", mp6_savestate_strerror(r));
                fflush(stderr);
            }
        }
        g_requestPath[0] = '\0';
        g_lastResultWasSave = (action == MP6_SS_PENDING_SAVE);
        g_lastResultCode = r;
        g_lastResultValid = 1;
        g_uiGeneration++;
    }
}
