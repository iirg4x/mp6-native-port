/* MP6 native port -- cross-session savestate capture/restore.
 * See shim/include/mp6_savestate.h for the design rationale and the
 * carve-out contract.
 *
 * FILE LAYOUT
 *   [Mp6SavestateHeader]            fixed-size, uncompressed, validated first
 *   [Mp6SavestateRegion  x nRegion] fixed-size table, uncompressed
 *   [deflate stream]                every region's bytes, concatenated in
 *                                   table order, compressed as ONE stream
 *
 * The payload is one continuous deflate stream rather than per-region
 * blobs because the arena is 256 MB of which the overwhelming majority is
 * untouched zeros -- a single stream lets those collapse across region
 * boundaries too, and keeps the writer a simple "feed everything through
 * once" loop instead of per-region bookkeeping.
 *
 * FAIL-CLOSED DISCIPLINE. mp6_savestate_restore() validates the header,
 * every base address, and the build fingerprint, and fully decompresses
 * into scratch memory BEFORE writing a single byte of live game memory.
 * A corrupt, truncated, foreign-build, or differently-based state file can
 * therefore only ever produce a clean error return -- never a half-restored
 * process. This mirrors the existing mp6_assert_image_low convention: prove
 * the invariant or refuse, never proceed on a maybe.
 */
#include "mp6_savestate.h"

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "host.h"
#include "zlib.h"
#include "mp6_boot.h" /* mp6_max_ticks/mp6_ticks_unlimited -- latched across restore */

/* SAVESTATE CARVE-OUT. Placing this AFTER this TU's own
 * includes is load-bearing, not stylistic: it is a #pragma clang section that
 * redirects every file-scope definition FOLLOWING it. As a -include (before all
 * headers) it also captured the decomp headers' C TENTATIVE definitions --
 * dolphin/os.h:27 `u32 __OSBusClock;` and friends -- turning those common
 * symbols into strong per-TU definitions and breaking the link with duplicate
 * symbol errors. Here, headers keep their normal linkage and only this file's
 * own statics move. tools/build.py asserts this line exists in every TU listed
 * in HOST_STATE_SECTION_SOURCES. */
#include "mp6_host_section.h"


/* Arena accessors (platform/os/arena.c). Declared here rather than in a
 * header because arena.c exposes no header of its own; these two have
 * existed as an ABI with no consumer until now. */
extern void *mp6_arena_base(void);
extern unsigned int mp6_arena_size(void);

/* The build fingerprint: a sha1 over EVERY link input (all objects, plus the
 * aurora archives/import libs on the windowed row), computed by build.py at
 * link time and injected via a tiny autogenerated TU. A savestate is only
 * meaningful against the exact binary that produced it -- a rebuild moves
 * code and data even at a fixed image base, so a captured coroutine stack's
 * return addresses would point into the wrong functions.
 *
 * This REPLACES an earlier __DATE__/__TIME__-of-this-TU stamp, which a code
 * review confirmed was a broken guard: build.py's needs_rebuild() only
 * recompiles savestate.o when savestate.c itself (or build.py, or a shared
 * header tree) changes, so editing any OTHER .c relinked a new exe still
 * carrying the old timestamp -- and the same-binary check silently accepted
 * a stale state against moved code. A link-input hash cannot go stale that
 * way, and has the bonus that a no-op rebuild keeps existing states valid
 * (identical inputs, identical hash) where a timestamp would have
 * invalidated them for nothing. */
extern const char mp6_link_stamp[]; /* build/mp6_link_stamp_<mode>.c, autogenerated */

/* Address anchor for the image-base proxy below -- any static in this TU
 * works; it only has to live at a fixed link-time address. */
static const char kImageAnchor[] = "mp6-savestate-anchor";

/* Set once a restore succeeds. Lives in this file's carved-out statics, so
 * it describes the RUNNING process (a restored state can never smuggle in
 * a stale value of it). */
static int g_wasRestored;

int mp6_savestate_was_restored(void) { return g_wasRestored; }

void mp6_savestate_guarded_exit(int code)
{
    fflush(stdout);
    fflush(stderr);
    if (g_wasRestored) {
        /* Third-party teardown over restored state is the one crash class the
         * denylist carve-out cannot fully close (static CRT globals are
         * un-annotatable) -- so a restored process does not run it at all.
         * See mp6_savestate.h's guarded-exit comment. */
        _exit(code);
    }
    exit(code);
}

#define MP6_SAVESTATE_MAX_REGIONS 160

/* Region kinds, recorded so a restore can apply per-kind policy and so a
 * human reading a hexdump can tell what they are looking at. */
enum {
    MP6_SS_REGION_ARENA = 1,
    MP6_SS_REGION_IMAGE = 2, /* one writable PE section (never the carve-out) */
    MP6_SS_REGION_CORO  = 3, /* one in-use coroutine pool slot */
    MP6_SS_REGION_ARAM  = 4  /* the fake-ARAM backing buffer -- host heap, UNPINNED address */
};

/* The fake-ARAM buffer (platform/null/shims_manual.c). Declared here rather
 * than in a header because shims_manual.c publishes no header of its own. */
extern void *mp6_aram_savestate_buffer(size_t *sizeOut);

typedef struct {
    uint32_t kind;
    uint32_t index;   /* coro slot index for CORO; else 0 */
    uint64_t addr;    /* absolute address at capture time -- must match on load */
    uint64_t size;
    char     name[16];
} Mp6SavestateRegion;

typedef struct {
    uint32_t magic;
    uint32_t version;
    char     buildStamp[48]; /* sha1 hex (40) + NUL, from mp6_link_stamp */
    uint64_t arenaBase;
    uint64_t arenaSize;
    uint64_t coroPoolBase;
    uint64_t coroPoolSize;
    uint64_t imageBase;
    uint32_t regionCount;
    uint32_t reserved;
    uint64_t rawBytes;        /* sum of region sizes, for the decompress buffer */
    uint64_t compressedBytes; /* deflate stream length that follows the table --
                               * ALSO the offset key for the widescreen blob
                               * below (blob = header + table + this many bytes) */
    uint64_t wsNativeBytes;   /* widescreen native-snapshot blob, stored
                               * UNCOMPRESSED after the deflate stream (small --
                               * KBs of vertex floats) */
    /* the mixer's own view at capture time. Carried in the header rather
     * than as a region because it is small, fixed-size, and -- unlike every
     * region -- has no address to be restored TO; it is replayed through the
     * msm API instead. See mp6_savestate.h for why it must be captured at
     * all (the playing BGM stream id exists nowhere else in the process). */
    Mp6SsAudioShadow audio;
} Mp6SavestateHeader;

