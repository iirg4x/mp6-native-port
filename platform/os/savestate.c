const char *mp6_savestate_strerror(int result)
{
    switch (result) {
    case MP6_SAVESTATE_OK:                    return "ok";
    case MP6_SAVESTATE_ERR_IO:                return "file I/O failed";
    case MP6_SAVESTATE_ERR_FORMAT:            return "not a savestate, or truncated/wrong version";
    case MP6_SAVESTATE_ERR_BINARY_MISMATCH:   return "captured by a DIFFERENT build of the exe -- rebuild invalidates savestates";
    case MP6_SAVESTATE_ERR_LAYOUT_MISMATCH:   return "memory landed at a different address this run -- state cannot be applied";
    case MP6_SAVESTATE_ERR_UNSUPPORTED:       return "no capturable coroutine pool (fiber backend)";
    case MP6_SAVESTATE_ERR_NOMEM:             return "out of memory";
    default:                                  return "unknown error";
    }
}

/* Image base, as the savestate means it: the load address of the running
 * module. Derived from a known-static address rather than re-querying the
 * OS so this file needs no windows.h (host_win32.c is the only TU allowed
 * to include it). Any writable section's address is a fixed offset from the
 * image base, so section addresses are compared individually anyway -- this
 * value exists for the error message and for a fast early reject. */
static uint64_t mp6_ss_image_base_proxy(void)
{
    return (uint64_t)(uintptr_t)&kImageAnchor[0];
}

/* Collects the regions this build would capture. Shared by capture and
 * restore so the two can never disagree about what the set should be --
 * restore then checks the file's table against this. */
static int mp6_ss_collect_regions(Mp6SavestateRegion *out, int maxOut, uint64_t *rawBytesOut)
{
    Mp6HostImageSection sections[64];
    int nSect, i, n = 0;
    uint64_t raw = 0;

    if (mp6_arena_base() == NULL || mp6_coro_pool_base() == NULL) {
        return -1;
    }

    /* 1. The game arena, whole. Capturing all 256 MB rather than only the
     * live heap prefix is deliberate: the arena's bump allocation is
     * per-heap, not arena-global, so "the live set" is a union of ranges
     * whose upper bounds sit near the end of each heap almost immediately
     * after boot -- the naive prefix would save far less than it appears
     * to, while adding a way to under-capture. The untouched remainder is
     * zeros, which deflate collapses to nearly nothing. */
    if (n >= maxOut) return -1;
    out[n].kind = MP6_SS_REGION_ARENA;
    out[n].index = 0;
    out[n].addr = (uint64_t)(uintptr_t)mp6_arena_base();
    out[n].size = (uint64_t)mp6_arena_size();
    snprintf(out[n].name, sizeof(out[n].name), "arena");
    raw += out[n].size;
    n++;

    /* 2. Every writable image section EXCEPT the host-owned carve-out. */
    nSect = mp6_host_image_writable_sections(sections, (int)(sizeof(sections) / sizeof(sections[0])));
    if (nSect <= 0) {
        return -1;
    }
    for (i = 0; i < nSect; i++) {
        if (strcmp(sections[i].name, MP6_HOST_STATE_SECTION_BSS) == 0 ||
            strcmp(sections[i].name, MP6_HOST_STATE_SECTION_DATA) == 0) {
            continue; /* host-thread-owned / OS handles -- see mp6_savestate.h */
        }
        if (strcmp(sections[i].name, ".tls") == 0) {
            /* Thread-local storage TEMPLATE data, not the live per-thread
             * copies. It belongs to the host threading model (and the
             * #pragma clang section carve-out provably cannot reach TLS
             * variables), so restoring it can only ever install another
             * process's thread-local seed values over this one's. */
            continue;
        }
        if (n >= maxOut) return -1;
        out[n].kind = MP6_SS_REGION_IMAGE;
        out[n].index = 0;
        out[n].addr = (uint64_t)(uintptr_t)sections[i].addr;
        out[n].size = (uint64_t)sections[i].size;
        snprintf(out[n].name, sizeof(out[n].name), "%s", sections[i].name);
        raw += out[n].size;
        n++;
    }

    /* 2b. The fake-ARAM backing buffer. It is 16 MB of host calloc, so
     * neither the arena sweep nor the image sweep covers it -- and its
     * CONTENTS are the load-bearing part, not just its pointer. game/armem.c's
     * residency table (ARInfo/ARBase) is ordinary decomp .bss and IS
     * restored, while the bytes it indexes are not; HuAR_DVDtoARAM early-outs
     * on HuARDirCheck, so a rewound table claims "already cached" and the DMA
     * that would refill ARAM never runs again. Capturing only the pointer
     * (or merely re-pointing it) would therefore trade a crash for SILENT
     * asset corruption on the same path. The address recorded here is
     * advisory -- restore copies into whatever buffer the live process
     * allocated (see the commit loop). */
    {
        size_t aramSize = 0;
        void *aram = mp6_aram_savestate_buffer(&aramSize);
        if (aram != NULL && aramSize > 0) {
            if (n >= maxOut) return -1;
            out[n].kind = MP6_SS_REGION_ARAM;
            out[n].index = 0;
            out[n].addr = (uint64_t)(uintptr_t)aram;
            out[n].size = (uint64_t)aramSize;
            snprintf(out[n].name, sizeof(out[n].name), "aram");
            raw += out[n].size;
            n++;
        }
    }

    /* 3. Each in-use coroutine slot, whole (header + stack). */
    for (i = 0; i < mp6_coro_slot_count(); i++) {
        if (!mp6_coro_slot_in_use(i)) {
            continue;
        }
        if (n >= maxOut) return -1;
        out[n].kind = MP6_SS_REGION_CORO;
        out[n].index = (uint32_t)i;
        out[n].addr = (uint64_t)(uintptr_t)mp6_coro_slot_addr(i);
        out[n].size = (uint64_t)mp6_coro_slot_size();
        snprintf(out[n].name, sizeof(out[n].name), "coro%d", i);
        raw += out[n].size;
        n++;
    }

    if (rawBytesOut) {
        *rawBytesOut = raw;
    }
    return n;
}

