int mp6_savestate_capture(const char *path)
{
    Mp6SavestateRegion regions[MP6_SAVESTATE_MAX_REGIONS];
    Mp6SavestateHeader hdr;
    uint64_t rawBytes = 0;
    int nRegion, i, rc = MP6_SAVESTATE_OK;
    FILE *f = NULL;
    z_stream zs;
    unsigned char *outBuf = NULL;
    const size_t kChunk = 1u << 20;
    int zInit = 0;
    char tmpPath[560];

    if (path == NULL) {
        return MP6_SAVESTATE_ERR_IO;
    }
    if (mp6_coro_pool_base() == NULL) {
        return MP6_SAVESTATE_ERR_UNSUPPORTED;
    }
    /* zero the table before filling. collect fills name[16]
     * via snprintf, which writes only the chars + NUL -- without this memset
     * the tails are uninitialized stack bytes, fwritten into a file users
     * hand to other people (nondeterministic artifacts + a small info leak). */
    memset(regions, 0, sizeof(regions));
    nRegion = mp6_ss_collect_regions(regions, MP6_SAVESTATE_MAX_REGIONS, &rawBytes);
    if (nRegion <= 0) {
        /* collect failing means the platform cannot enumerate
         * its regions (Android's writable-section walk returns 0 by design)
         * -- that is "unsupported here", not "corrupt file". ERR_FORMAT's
         * message ("not a savestate, or truncated") sent users chasing file
         * corruption for a file that was never opened. */
        return MP6_SAVESTATE_ERR_UNSUPPORTED;
    }

    /* A load-bearing assumption, checked instead of trusted: the DVD
     * layer's open-handle table must be empty at any capture point, because
     * that table is carved out and records no path/entrynum, so an open file
     * cannot be re-established on restore. It is empty in this build (no
     * compiled caller holds a DVD file across a frame boundary -- see
     * platform/dvd/dvd_files.c's own comment). Enabling THP or compiling
     * msmstream.c would break that, and this is how you would find out:
     * loudly, at capture, rather than as a mystery corruption on load. */
    if (mp6_dvd_open_handle_count() != 0) {
        fprintf(stderr,
                "[SAVESTATE] WARNING: %d DVD file handle(s) open at capture. Restored state "
                "will refer to files this savestate cannot reopen (the table needs splitting "
                "into {key,entrynum} + a carved-out {FILE*}).\n",
                mp6_dvd_open_handle_count());
        fflush(stderr);
    }

    memset(&hdr, 0, sizeof(hdr));
    hdr.magic = MP6_SAVESTATE_MAGIC;
    hdr.version = MP6_SAVESTATE_VERSION;
    snprintf(hdr.buildStamp, sizeof(hdr.buildStamp), "%s", mp6_link_stamp);
    hdr.arenaBase = (uint64_t)(uintptr_t)mp6_arena_base();
    hdr.arenaSize = (uint64_t)mp6_arena_size();
    hdr.coroPoolBase = (uint64_t)(uintptr_t)mp6_coro_pool_base();
    hdr.coroPoolSize = (uint64_t)mp6_coro_pool_size();
    hdr.imageBase = mp6_ss_image_base_proxy();
    hdr.regionCount = (uint32_t)nRegion;
    hdr.rawBytes = rawBytes;
    hdr.compressedBytes = 0; /* patched below once known */
    mp6_msm_savestate_capture(&hdr.audio);

    /* S2 (review): write to a TEMP file and rename over the slot only after
     * the whole capture is proven on disk. fopen(path,"wb") truncated the
     * user's existing good state in place, so a failed capture -- disk full,
     * AV lock, a crash mid-deflate -- destroyed the very repro they meant to
     * keep. save and load share one slot path, which makes that loss total. */
    snprintf(tmpPath, sizeof(tmpPath), "%s.tmp", path);
    f = fopen(tmpPath, "wb");
    if (f == NULL) {
        return MP6_SAVESTATE_ERR_IO;
    }
    if (fwrite(&hdr, sizeof(hdr), 1, f) != 1 ||
        fwrite(regions, sizeof(regions[0]), (size_t)nRegion, f) != (size_t)nRegion) {
        rc = MP6_SAVESTATE_ERR_IO;
        goto done;
    }

    outBuf = (unsigned char *)malloc(kChunk);
    if (outBuf == NULL) {
        rc = MP6_SAVESTATE_ERR_NOMEM;
        goto done;
    }
    memset(&zs, 0, sizeof(zs));
    /* Level 1: this is a debugging tool run interactively, and the bulk of
     * the payload is long zero runs that even the fastest level collapses
     * completely. Spending seconds of the user's time on level 9 to shave
     * a mostly-zero stream is the wrong trade. */
    if (deflateInit(&zs, 1) != Z_OK) {
        rc = MP6_SAVESTATE_ERR_NOMEM;
        goto done;
    }
    zInit = 1;

    for (i = 0; i < nRegion; i++) {
        zs.next_in = (Bytef *)(uintptr_t)regions[i].addr;
        zs.avail_in = (uInt)regions[i].size;
        /* Region sizes here are well under 4 GB each, so one avail_in pass
         * per region is safe; the loop below drains as many output chunks
         * as that input produces. */
        while (zs.avail_in > 0) {
            zs.next_out = outBuf;
            zs.avail_out = (uInt)kChunk;
            if (deflate(&zs, Z_NO_FLUSH) != Z_OK) {
                rc = MP6_SAVESTATE_ERR_IO;
                goto done;
            }
            {
                size_t produced = kChunk - zs.avail_out;
                if (produced && fwrite(outBuf, 1, produced, f) != produced) {
                    rc = MP6_SAVESTATE_ERR_IO;
                    goto done;
                }
            }
        }
    }
    /* Flush the deflate tail. */
    for (;;) {
        int zr;
        size_t produced;
        zs.next_out = outBuf;
        zs.avail_out = (uInt)kChunk;
        zr = deflate(&zs, Z_FINISH);
        produced = kChunk - zs.avail_out;
        if (produced && fwrite(outBuf, 1, produced, f) != produced) {
            rc = MP6_SAVESTATE_ERR_IO;
            goto done;
        }
        if (zr == Z_STREAM_END) {
            break;
        }
        if (zr != Z_OK) {
            rc = MP6_SAVESTATE_ERR_IO;
            goto done;
        }
    }

    hdr.compressedBytes = (uint64_t)zs.total_out;

    /* append the widescreen native-snapshot blob (see
     * mp6_widescreen_savestate_blob_size's comment for why these host-heap
     * buffers are game state that must travel in the file). Uncompressed --
     * it is KBs against a multi-MB file. */
    {
        size_t wsSize = mp6_widescreen_savestate_blob_size();
        void *wsBuf = malloc(wsSize > 0 ? wsSize : 1);
        if (wsBuf == NULL) {
            rc = MP6_SAVESTATE_ERR_NOMEM;
            goto done;
        }
        mp6_widescreen_savestate_blob_write(wsBuf);
        if (wsSize > 0 && fwrite(wsBuf, 1, wsSize, f) != wsSize) {
            free(wsBuf);
            rc = MP6_SAVESTATE_ERR_IO;
            goto done;
        }
        free(wsBuf);
        hdr.wsNativeBytes = (uint64_t)wsSize;
    }

    if (fseek(f, 0, SEEK_SET) != 0 || fwrite(&hdr, sizeof(hdr), 1, f) != 1) {
        rc = MP6_SAVESTATE_ERR_IO;
        goto done;
    }

    /* the final buffered chunk (and the rewritten header)
     * only reach the disk at flush/close -- check BOTH before claiming
     * success, or a disk-full/AV-lock truncation ships as a "captured"
     * file that fails on the recipient's machine with a corruption error. */
    if (fflush(f) != 0 || ferror(f) || fclose(f) != 0) {
        f = NULL; /* closed (or unusable) either way -- don't double-close */
        rc = MP6_SAVESTATE_ERR_IO;
        goto done;
    }
    f = NULL;
    /* Atomic-enough publish: drop any previous slot content, then rename the
     * fully-written temp into place. On any failure the old state may already
     * be gone (remove succeeded, rename failed -- a genuinely torn window),
     * but the temp file still holds the complete new capture, and the error
     * message names it so nothing is lost. */
    remove(path);
    if (rename(tmpPath, path) != 0) {
        fprintf(stderr, "[SAVESTATE] rename failed -- the capture is intact at %s\n", tmpPath);
        rc = MP6_SAVESTATE_ERR_IO;
        goto done;
    }

    printf("[SAVESTATE] captured %s: %d regions, %.1f MB raw -> %.1f MB compressed\n",
           path, nRegion,
           (double)rawBytes / (1024.0 * 1024.0),
           (double)zs.total_out / (1024.0 * 1024.0));
    fflush(stdout);

done:
    if (zInit) {
        deflateEnd(&zs);
    }
    free(outBuf);
    if (f) {
        fclose(f);
        remove(tmpPath); /* failed capture: clean up the partial temp, leave the old slot alone */
    }
    return rc;
}

